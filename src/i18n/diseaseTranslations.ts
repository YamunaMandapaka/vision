// Advanced translation with word-by-word fallback
export function translateText(text: string, targetLang: string): string {
  if (targetLang === 'en' || !text) return text;
  
  let translated = text;
  
  // Sort by length (longest first) to match phrases before individual words
  const entries = Object.entries(translationDict).sort((a, b) => b[0].length - a[0].length);
  
  for (const [english, translations] of entries) {
    if (translations[targetLang]) {
      // Case-insensitive replacement
      const regex = new RegExp(english, 'gi');
      translated = translated.replace(regex, translations[targetLang]);
    }
  }
  
  return translated;
}

